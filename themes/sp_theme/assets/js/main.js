function getStoredTheme() {
  try {
    return localStorage.getItem("theme");
  } catch {
    return null;
  }
}

function setStoredTheme(theme) {
  try {
    localStorage.setItem("theme", theme);
  } catch {
    // Ignore storage failures (e.g. private mode restrictions)
  }
}

function applyTheme(theme) {
  if (theme === "dark") {
    document.documentElement.classList.add("dark");
  } else {
    document.documentElement.classList.remove("dark");
  }
}

function initializeTheme() {
  const storedTheme = getStoredTheme();
  const initialTheme = storedTheme === "dark" ? "dark" : "light";
  applyTheme(initialTheme);

  // Persist explicit light preference so mode is never inferred from system.
  if (!storedTheme) {
    setStoredTheme("light");
  }
}

function updateThemeToggleButton(button, isDark) {
  const icon = button.querySelector("#theme-toggle-icon");
  const iconSvg = isDark
    ? '<svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="1.8" stroke="currentColor" class="size-5"><path stroke-linecap="round" stroke-linejoin="round" d="M21.752 15.002A9.718 9.718 0 0 1 18 15.75c-5.385 0-9.75-4.365-9.75-9.75 0-1.33.266-2.597.748-3.752A9.753 9.753 0 1 0 21.752 15.002Z"/></svg>'
    : '<svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="1.8" stroke="currentColor" class="size-5"><path stroke-linecap="round" stroke-linejoin="round" d="M12 3v1.5m0 15V21m9-9h-1.5m-15 0H3m15.364 6.364-1.06-1.06M6.697 6.697 5.636 5.636m12.728 0-1.06 1.061M6.697 17.303l-1.06 1.06M15.75 12a3.75 3.75 0 1 1-7.5 0 3.75 3.75 0 0 1 7.5 0Z"/></svg>';

  button.setAttribute("aria-pressed", String(isDark));
  button.setAttribute(
    "aria-label",
    isDark ? "Switch to light mode" : "Switch to dark mode",
  );

  if (icon) {
    icon.classList.remove("animate-theme-icon-switch");
    icon.innerHTML = iconSvg;
    void icon.offsetWidth;
    icon.classList.add("animate-theme-icon-switch");
  }
}

// Global utilities and device detection
const DeviceUtils = {
  isIOS: () => /iPad|iPhone|iPod/.test(navigator.userAgent) && !window.MSStream,
  isMac: () => navigator.platform.toUpperCase().indexOf("MAC") >= 0,
  isAppleDevice: function () {
    return this.isIOS() || this.isMac();
  },
  getUserAgent: () => navigator.userAgent.toLowerCase(),
};

// Email functionality with device-specific handling
function openEmail() {
  const to = "thea@storypad.me";
  const subject = "Feedback / Support Request";
  const body = `Hi Thea,

I'd like to:
- Report a bug 🐞
- Request a feature ✨
- Share a suggestion 💡
- Ask for support 🙋‍♀️

(Please fill in your message here.)

Thanks!`;

  const gmailWebURL = `https://mail.google.com/mail/?view=cm&fs=1&to=${encodeURIComponent(
    to,
  )}&su=${encodeURIComponent(subject)}&body=${encodeURIComponent(body)}`;
  const mailtoURL = `mailto:${encodeURIComponent(
    to,
  )}?subject=${encodeURIComponent(subject)}&body=${encodeURIComponent(body)}`;

  const ua = DeviceUtils.getUserAgent();

  if (/android/.test(ua)) {
    // Android Gmail app intent
    const gmailIntentURL = `intent:mailto:${encodeURIComponent(
      to,
    )}?subject=${encodeURIComponent(subject)}&body=${encodeURIComponent(
      body,
    )}#Intent;scheme=mailto;package=com.google.android.gm;end`;
    window.location.href = gmailIntentURL;
  } else if (/iphone|ipad|ipod/.test(ua)) {
    // iOS Gmail app scheme fallback to mailto
    const gmailIOSURL = `googlegmail://co?to=${encodeURIComponent(
      to,
    )}&subject=${encodeURIComponent(subject)}&body=${encodeURIComponent(body)}`;
    window.location.href = gmailIOSURL;
    setTimeout(() => {
      window.location.href = mailtoURL;
    }, 1500);
  } else if (/windows/.test(ua) || /linux/.test(ua) || /macintosh/.test(ua)) {
    // Desktop OS: open Gmail web or fallback to mailto
    const newWindow = window.open(gmailWebURL, "_blank");
    if (
      !newWindow ||
      newWindow.closed ||
      typeof newWindow.closed === "undefined"
    ) {
      window.location.href = mailtoURL;
    }
  } else if (ua.includes("chrome")) {
    // Chrome desktop: open Gmail web or fallback to mailto
    const newWindow = window.open(gmailWebURL, "_blank");
    if (
      !newWindow ||
      newWindow.closed ||
      typeof newWindow.closed === "undefined"
    ) {
      window.location.href = mailtoURL;
    }
  } else {
    // Default fallback
    window.location.href = mailtoURL;
  }
}

// Header functionality
function initializeHeader() {
  const header = document.querySelector("header");
  if (header) {
    const toggleHeaderBorder = () => {
      const hasScrolledPastThreshold = window.scrollY > 100;
      header.classList.toggle("border-b-1", hasScrolledPastThreshold);
      header.classList.toggle("border-b-neutral-300", hasScrolledPastThreshold);
      header.classList.toggle(
        "dark:border-b-neutral-600",
        hasScrolledPastThreshold,
      );
    };

    toggleHeaderBorder();
    window.addEventListener("scroll", toggleHeaderBorder, { passive: true });
  }

  const themeToggle = document.querySelector("button#theme-toggle");
  if (themeToggle) {
    const isDarkInitially = document.documentElement.classList.contains("dark");
    updateThemeToggleButton(themeToggle, isDarkInitially);

    themeToggle.addEventListener("click", function () {
      const nextIsDark = !document.documentElement.classList.contains("dark");
      applyTheme(nextIsDark ? "dark" : "light");
      setStoredTheme(nextIsDark ? "dark" : "light");
      updateThemeToggleButton(this, nextIsDark);
    });
  }

  const headerTitle = document.querySelector(
    'header h1[data-action="scroll-to-top"]',
  );
  if (headerTitle) {
    headerTitle.addEventListener("click", function () {
      if (window.scrollY === 0) {
        this.classList.remove("animate-shake-vertical");
        void this.offsetWidth; // Trigger reflow
        this.classList.add("animate-shake-vertical");
      } else {
        window.scrollTo({ top: 0, behavior: "smooth" });
      }
    });
  }

  // Setup download buttons - show only the relevant one for the platform
  const iosLink = document.querySelector("a#download-link-ios");
  const androidLink = document.querySelector("a#download-link-android");

  if (iosLink && androidLink) {
    if (DeviceUtils.isAppleDevice()) {
      androidLink.style.display = "none";
    } else {
      iosLink.style.display = "none";
    }
  }
}

// Footer functionality
function initializeFooter() {
  // Setup scroll to top functionality
  const logoLink = document.querySelector(
    'footer [data-action="scroll-to-top"]',
  );
  if (logoLink) {
    logoLink.addEventListener("click", function () {
      window.scrollTo({ top: 0, behavior: "smooth" });
    });
  }

  // Setup email link
  const emailLink = document.querySelector('a[data-action="open-email"]');
  if (emailLink) {
    emailLink.addEventListener("click", function (e) {
      e.preventDefault();
      openEmail();
    });
  }
}

// Use case cards functionality
function initializeUseCaseCards() {
  const cards = document.querySelectorAll(".home-usecase-card");

  cards.forEach((card) => {
    // Add click handler for touch devices
    card.addEventListener("click", function () {
      if (!window.matchMedia("(hover: hover)").matches) {
        document.querySelectorAll(".home-usecase-card").forEach((c) => {
          if (c !== this) {
            c.classList.remove("active");
          }
        });
        this.classList.toggle("active");
      }
    });

    // Add hover handler for devices with hover capability
    card.addEventListener("mouseenter", function () {
      document.querySelectorAll(".home-usecase-card").forEach((c) => {
        if (c !== this) {
          c.classList.remove("active");
        }
      });
    });

    // Handle image interactions within cards
    const images = card.querySelectorAll("img[data-action]");
    images.forEach((img) => {
      const action = img.getAttribute("data-action");

      img.addEventListener("click", function () {
        if (action === "scroll-container") {
          // Scrollable image handling
          const container = this.parentElement;
          const shouldScrollToTop =
            container.scrollTop + container.clientHeight >=
            container.scrollHeight - 50;
          container.scrollTo({
            top: shouldScrollToTop ? 0 : container.scrollTop + 240,
            behavior: "smooth",
          });
        } else if (action === "shake-animation") {
          // Shake animation handling
          this.classList.remove("animate-shake-vertical");
          void this.offsetWidth; // Trigger reflow
          this.classList.add("animate-shake-vertical");
        } else if (action === "carousel-scroll") {
          // Image carousel handling
          if (this.dataset.last === "yes") {
            this.parentElement.scrollTo({ left: 0, behavior: "smooth" });
          } else {
            this.parentElement.scrollBy({
              left: this.offsetWidth,
              behavior: "smooth",
            });
          }
        }
      });
    });
  });
}

